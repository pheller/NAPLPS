    static DrawableAsciiChar()
    {
        var assembly = typeof(DrawableAsciiChar).Assembly;

        // Use PRM5X10 as the base font — it's the "standard" NAPLPS font
        using var stream = assembly.GetManifestResourceStream("NAPLPS.Fonts.PRM5X10.TTF");

        if (stream == null)
        {
            throw new InvalidOperationException("Could not load embedded font resource.");
        }

        _fontFamily = _fontCollection.Add(stream);

        // Measure reference bounds at size 100
        var refFont = _fontFamily.CreateFont(100f, FontStyle.Regular);
        var refBounds = TextMeasurer.MeasureBounds("Mg", new TextOptions(refFont));
        var refWidthBounds = TextMeasurer.MeasureBounds("M", new TextOptions(refFont));

        _refLineHeight = refBounds.Height;
        _refTopOffset = refBounds.Top;
        _refCharWidth = refWidthBounds.Width;

        var refCapBounds = TextMeasurer.MeasureBounds("H", new TextOptions(refFont));
        _refCapHeight = refCapBounds.Height;

        // Load a system fallback font for supplementary characters not in PRM5X10
        string[] fallbackNames = ["Segoe UI", "Arial", "DejaVu Sans", "Liberation Sans", "Noto Sans"];
        foreach (var name in fallbackNames)
        {
            if (SystemFonts.TryGet(name, out var family))
            {
                _fallbackFontFamily = family;
                break;
            }
        }
    }

    /// <summary>
    /// Gets the appropriate font family for a character.
    /// Uses PRM5X10 for ASCII (0x20-0x7E), falls back to system font for supplementary.
    /// </summary>
    private static FontFamily GetFontForChar(char c)
    {
        if (c >= 0x20 && c <= 0x7E)
        {
            return _fontFamily;
        }

        return _fallbackFontFamily ?? _fontFamily;
    }

    /// <summary>
    /// Gets the width ratio of a character relative to full cell width.
    /// Simplified version using row 8 lookup. Used for rendering cell sizing.
    /// </summary>
    public static float GetCharWidthRatio(char c)
    {
        if (c < 0x20 || c > 0x7E)
        {
            return 1.0f; // Full width for unknown characters
        }

        int widthClass = _asciiWidthClass[c - 0x20];
        return _displacementRow8[widthClass] / 8f;
    }

    /// <summary>
    /// ANSI X3.110 proportional spacing algorithm.
    /// Returns the cursor displacement as a normalized coordinate value,
    /// given the character field width and the character's width class.
    /// </summary>
    public static float GetProportionalDisplacement(float charFieldWidth, char c)
    {
        // Get width class for this character
        int widthClass;
        if (c >= 0x20 && c <= 0x7E)
        {
            widthClass = _asciiWidthClass[c - 0x20];
        }
        else
        {
            widthClass = 9; // Full width for supplementary/unknown
        }

        // Multiply character field width by 256, truncate to integer
        int n = (int)(charFieldWidth * 256f);

        if (n < 6)
        {
            // Below minimum table row — use full field width for class 9, scale for others
            return charFieldWidth * (widthClass + 1) / 10f;
        }

        if (n < 12)
        {
            // Phase 1: Small text — direct table lookup
            int row = n - 6; // Table rows 0-5 correspond to n=6-11
            if (row >= 0 && row < 6)
            {
                int disp = _displacementTable[row, widthClass];
                return disp / 256f;
            }
        }

        // Phase 2: Large text (n >= 12) — scaling algorithm
        // Step 1: Truncate to 8 MSBs (DOMAIN 3 precision)
        int n8 = n & 0xFF; // Already 8-bit from the multiply-by-256

        // Step 2: Multiply by 11/13
        int scaled = (n8 * 11) / 13;

        // Step 3: Subtract 1/256, OR with 1/256, subtract 1/256 again
        // This forces the result to be odd (in 1/256 units)
        int f = scaled;
        f = f - 1;
        f = f | 1;
        f = f - 1;
        f = f & 0xFF; // Truncate to 8 bits

        // Step 4: Get unit spacing from row 12
        int unitSpacing = _unitSpacingRow12[widthClass];

        // Step 5: Multiply unit spacing by f
        int product = unitSpacing * f;

        // Step 6: Divide by 6, add 1/512 for rounding, truncate to 8 bits
        int adjustment = ((product / 6) + 1) >> 1; // +1 then >>1 approximates +0.5 rounding
        adjustment = adjustment & 0xFF;

        // Displacement = n - adjustment
        int displacement = n8 - adjustment;

        return displacement / 256f;
    }

    /// <summary>
    /// Gets a horizontal stretch boost factor for thin characters.
    /// These characters have lots of built-in whitespace in the font and need
    /// extra stretching to fill their cells properly.
    /// </summary>
    private static float GetThinCharacterBoost(char c)
    {
        return c switch
        {
            'i' or 'l' or ':' or '.' or '\'' => 4.0f,   // Very thin - need 2x stretch
            '1' => 2.0f,  // Moderately thin
            't' or 'I' or '!' or '.' or ',' or ';' => 1.8f,  // Thin punctuation
            '(' or ')' or '[' or ']' or '|' => 1.6f,  // Brackets/pipe
            'j' => 1.4f,
            'f' => 1.2f,
            'w' or 'W' or 'X' or 'Y' or 'G' or 'm' or 'x' or 'M' or 'T' or '@' or '/' or '&' => 0.8f, // Too big
            'v' or 'V' => 0.6f, // Really big!
            _ => 1.0f,  // Normal characters - no boost
        };
    }

    private static void GetCharXBackoff(ref float offsetX, char character)
    {
        switch (character)
        {
            case 't':
            case '1':
            case ',':
            {
                offsetX -= 10;
            }
            break;

            case 'I':
            {
                offsetX -= 8;
            }
            break;

            case 'R':
            {
                offsetX -= 4;
            }
            break;
        }
    }

